# math_tools.py - obliczenia matematyczne

# ------------ SUMA ------------ 

def sumuj(a, b):
    """
    Dodaje dwie liczby.

    Argumenty: 
        a(float) - pierwsza liczba
        b(float) - druga liczba

    Zwraca:
        sumę a+b (float)
    """
    return a + b

# ------------ RÓŻNICA ------------

def odejmij(a, b):
    """
    Odejmuje dwie liczby.

    Argumenty: 
        a(float) - pierwsza liczba
        b(float) - druga liczba

    Zwraca:
        różnicę a-b (float)
    """
    return a - b

# ------------ ŚREDNIA ------------

def srednia(a, b):
    """
    Oblicza średnią z dwóch liczb.

    Argumenty: 
        a(float) - pierwsza liczba
        b(float) - druga liczba

    Zwraca:
        średnią z liczb a i b (float)
    """
    return (a + b) / 2